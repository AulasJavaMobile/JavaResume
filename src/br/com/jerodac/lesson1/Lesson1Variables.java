package br.com.jerodac.lesson1;

public class Lesson1Variables {

    public static void main(String[] args) {

        //Variaveis primitivas
        //7 tipos de variaveis

        int myInt; // recebe valores do tipo inteiro
        float myFloat; //recebe valores flutuantes (valores quebrados, numeros reais)
        double myDouble; //recebe valores reais, porem fornece um suporte para arredondamento
        long myLong; //para valores de alta expressão (Ex. milissegundos)
        boolean myBoolean; //Recebe valores logicos (True ou false)
        char myChar; //recebe uma cadeia unica de caracter
        byte myByte; //recebe um sinal de bit especifico, utilizado para escovação de bytes

    }
}
